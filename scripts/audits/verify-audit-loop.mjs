#!/usr/bin/env node
// verify-audit-loop.mjs
// Enforces that every audit finding under docs/audits/findings/<YYYY-MM>/ is complete and
// dispositioned: required sections present, no surviving placeholders, evidence cites a real repo
// file, and a Disposition Decision verb (accept|fix|wontfix|skip) is recorded.
//
// Also regenerates/validates docs/audits/INDEX.md (with --write-index it writes the file).
// On a repo with zero findings (the current state) this passes vacuously (exit 0).
//
// Usage: node scripts/audits/verify-audit-loop.mjs [--write-index]

import { writeFileSync } from "node:fs";
import { repoRoot, walk, read, exists, done, info, join } from "../_lib.mjs";

const NAME = "verify-audit-loop";
const FINDINGS_DIR = "docs/audits/findings";
const INDEX = "docs/audits/INDEX.md";

const REQUIRED_HEADINGS = ["## Summary", "## Evidence", "## Recommendation", "## Disposition Decision"];
const REQUIRED_LABELS = ["Severity:", "Disposition:"];
const PLACEHOLDERS = [/<TODO/i, /\bTBD\b/, /<accept\|fix\|wontfix\|skip>/, /<reason>/];

function findingFiles() {
  return walk(FINDINGS_DIR).filter((f) => f.endsWith(".md") && !f.endsWith("INDEX.md"));
}

function section(text, heading) {
  const idx = text.indexOf(heading);
  if (idx === -1) return null;
  const rest = text.slice(idx + heading.length);
  const next = rest.search(/\n## /);
  return (next === -1 ? rest : rest.slice(0, next)).trim();
}

function validate(file, failures) {
  const text = read(file);
  for (const h of REQUIRED_HEADINGS) if (!text.includes(h)) failures.push(`${file}: missing "${h}".`);
  for (const l of REQUIRED_LABELS) if (!text.includes(l)) failures.push(`${file}: missing label "${l}".`);
  for (const re of PLACEHOLDERS) {
    if (re.test(text)) {
      failures.push(`${file}: unfilled placeholder (${re}) — disposition the finding before committing.`);
      return;
    }
  }
  const evidence = section(text, "## Evidence") || "";
  const paths = evidence.match(/[A-Za-z0-9_./-]+\.(?:mjs|md|kt|kts|toml|json|xml|properties|ya?ml)/g) || [];
  if (!paths.some((p) => exists(p.replace(/^\.\//, "")))) {
    failures.push(`${file}: ## Evidence cites no real repo file.`);
  }
  const disp = section(text, "## Disposition Decision") || "";
  if (!/\b(accept|fix|wontfix|skip)\b/i.test(disp)) {
    failures.push(`${file}: ## Disposition Decision must record accept|fix|wontfix|skip.`);
  }
}

function buildIndex(files) {
  const rows = files
    .map((f) => {
      const t = read(f);
      const g = (re) => (t.match(re) || [, "?"])[1].trim();
      return {
        file: f,
        title: g(/^#\s*Audit Finding:\s*(.+)$/m),
        date: g(/\*\*Date:\*\*\s*([0-9-]+)/),
        severity: g(/\*\*Severity:\*\*\s*(.+)/),
        disposition: g(/\*\*Disposition:\*\*\s*(.+)/),
      };
    })
    .sort((a, b) => (a.date < b.date ? 1 : -1));

  let md = `<!-- GENERATED by scripts/audits/verify-audit-loop.mjs --write-index. Do not hand-edit. -->
# Audit Findings Index

Generated index of all audit findings under \`docs/audits/findings/<YYYY-MM>/\`. Regenerate with
\`node scripts/audits/verify-audit-loop.mjs --write-index\` (npm: \`audit:index\`).

**Total findings: ${rows.length}**

`;
  if (rows.length === 0) {
    md += "_No findings yet. Scaffold one with `node scripts/ops/create-audit-finding.mjs`._\n";
  } else {
    md += "| Date | Severity | Disposition | Title | Finding |\n|---|---|---|---|---|\n";
    for (const r of rows) md += `| ${r.date} | ${r.severity} | ${r.disposition} | ${r.title} | \`${r.file}\` |\n`;
  }
  return md;
}

function main() {
  const writeIndex = process.argv.includes("--write-index");
  const failures = [];
  const files = findingFiles();
  for (const f of files) validate(f, failures);

  const indexMd = buildIndex(files);
  const indexPath = join(repoRoot, INDEX);
  if (writeIndex) {
    writeFileSync(indexPath, indexMd);
    info(`wrote ${INDEX} (${files.length} finding${files.length === 1 ? "" : "s"}).`);
  } else if (!exists(INDEX)) {
    writeFileSync(indexPath, indexMd);
    info(`seeded missing ${INDEX}.`);
  }

  done(NAME, failures);
}

main();
